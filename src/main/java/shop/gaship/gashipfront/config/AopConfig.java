package shop.gaship.gashipfront.config;

import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
import org.springframework.context.annotation.EnableAspectJAutoProxy;
import shop.gaship.gashipfront.aspect.CheckAccessTokenExpireTimeAspect;

/**
 * The type Aop config.
 */
@Configuration
@EnableAspectJAutoProxy
public class AopConfig {

    /**
     * @return check access token expire time aspect
     * @author 조재철
     */
    @Bean
    public CheckAccessTokenExpireTimeAspect checkAccessTokenExpireTimeAspect() {
        return new CheckAccessTokenExpireTimeAspect();
    }

}
